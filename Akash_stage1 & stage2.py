a = int(input("Enter 1st Number:" ))
b = int(input("Enter 2nd Number:" ))
sum = a + b
print("Addition Is: ",sum)
sum = a - b
print("Subtraction Is: ",sum)
sum = a * b
print("Multiplication Is: ",sum)
sum = a / b
print("Division Is: ",sum)
if sum > 0:
    print("Possitive Number")
elif sum < 0:
    print("Negative Number")
else:
    print("Zero")
    

              